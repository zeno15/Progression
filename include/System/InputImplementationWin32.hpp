#ifndef INCLUDED_SYSTEM_INPUT_IMPLEMENTATION_WIN32_HPP_
#define INCLUDED_SYSTEM_INPUT_IMPLEMENTATION_WIN32_HPP_

#include <System/Vector2.hpp>
#include <System/Keyboard.hpp>
#include <System/Mouse.hpp>

namespace Window {

	class Window;

}

namespace System {

	////////////////////////////////////////////////////////////
	//
	//	Win32 implementation of mouse and keyboard
	//
	////////////////////////////////////////////////////////////
	class InputImplementationWin32 {
	public:

		////////////////////////////////////////////////////////////
		//
		//	Returns whether or not the given key is down
		//
		////////////////////////////////////////////////////////////
		static bool isKeyDown(Keyboard::Key _key);

		////////////////////////////////////////////////////////////
		//
		//	Returns whether or not the given mouse button is down
		//
		////////////////////////////////////////////////////////////
		static bool isMouseButtonDown(Mouse::Button _button);

		////////////////////////////////////////////////////////////
		//
		//	Gets the mouse position in desktop coordinates
		//
		////////////////////////////////////////////////////////////
		static Vector2<int> getMousePosition(void);

		////////////////////////////////////////////////////////////
		//
		//	Gets the mouse position relative to the given window
		//
		////////////////////////////////////////////////////////////
		static Vector2<int> getMousePosition(const Window::Window& _window);

		////////////////////////////////////////////////////////////
		//
		//	Sets the mouses position in desktop coordinates
		//
		////////////////////////////////////////////////////////////
		static void setMousePosition(const Vector2<int>& _position);

		////////////////////////////////////////////////////////////
		//
		//	Sets the mouses position relative to the given window
		//
		////////////////////////////////////////////////////////////
		static void setMousePosition(const Vector2<int>& _position, const Window::Window& _window);

	public:
		////////////////////////////////////////////////////////////
		//
		//	Converts windows system values into zeno key values
		//
		////////////////////////////////////////////////////////////
		static Keyboard::Key systemToZeno(int _sysKey);

		////////////////////////////////////////////////////////////
		//
		// Converts zeno key values into windows system values
		//
		////////////////////////////////////////////////////////////
		static int zenoToSystem(Keyboard::Key _zenoKey);
	};

}

#endif // INCLUDED_SYSTEM_INPUT_IMPLEMENTATION_WIN32_HPP_