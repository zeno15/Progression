#ifndef INCLUDED_ECS_COMPONENT_HPP_
#define INCLUDED_ECS_COMPONENT_HPP_

////////////////////////////////////////////////////////////
///
///	\namespace	ECS
///
////////////////////////////////////////////////////////////
namespace ECS {
	////////////////////////////////////////////////////////////
	///
	///	\brief  Class represent a component in an entity component system.
	///
	////////////////////////////////////////////////////////////
	class Component {
	public:

		////////////////////////////////////////////////////////////
		///
		///	\brief Constructor with owning entities Id.
		///
		///	\param	The Id of the entity that owns this component
		///
		////////////////////////////////////////////////////////////
		explicit Component(unsigned int _entityId);

		////////////////////////////////////////////////////////////
		///
		///	\brief Destructor
		///
		////////////////////////////////////////////////////////////	
		virtual ~Component();

		////////////////////////////////////////////////////////////
		///
		///	\brief Gets the Id of the entity this component belongs to.
		///
		///	\return	The entities Id.
		///
		////////////////////////////////////////////////////////////
		unsigned int getEntityId(void) const;

	private:
		unsigned int m_EntityId;			///<	The Id of the entity this component  belongs to.
	};
}

#endif // INCLUDED_ECS_COMPONENT_HPP_